export const toTitleCase = (str, includeAllCaps, includeMinorWords) => {
  includeAllCaps = includeAllCaps
    ? includeAllCaps == true
      ? true
      : false
    : false
  includeMinorWords = includeMinorWords
    ? includeMinorWords == true
      ? true
      : false
    : false
  var i, j, lowers
  if (str === undefined || str === null) {
    str = ''
  }
  str = str.replace(/([^\W_]+[^\s-]*) */g, function (txt) {
    if (!/[a-z]/.test(txt) && /[A-Z]/.test(txt) && !includeAllCaps) {
      return txt
    } else {
      return txt.charAt(0).toUpperCase() + txt.substr(1).toLowerCase()
    }
  })

  if (includeMinorWords) {
    return str
  } else {
    // Certain minor words should be left lowercase unless
    // they are the first or last words in the string
    lowers = [
      'A',
      'An',
      'The',
      'And',
      'But',
      'Or',
      'For',
      'Nor',
      'As',
      'At',
      'By',
      'For',
      'From',
      'In',
      'Into',
      'Near',
      'Of',
      'On',
      'Onto',
      'To',
      'With',
    ]
    for (i = 0, j = lowers.length; i < j; i++)
      str = str.replace(
        new RegExp('\\s' + lowers[i] + '\\s', 'g'),
        function (txt) {
          return txt.toLowerCase()
        }
      )

    return str
  }
}

export const findPatients = (word, dataBase) => {
  if (word === undefined || word === null) {
    word = ''
  }
  const filteredData = dataBase.filter((item) => {
    const titleCaseKeyword = toTitleCase(word)
    if (item.FirstName.includes(titleCaseKeyword)) {
      return item
    }
  })
  // console.log('filtered data from fake data', filteredData)
  return filteredData
}

